package collectionlinkedList;

public class Book {

	int id;
	String bookName;
	String subject;
	String branch;
	int price;
	
	Book(int id ,  String bookName,  String subject ,  String branch , int price)
	{
		this.id = id;
		this.bookName = bookName;
		this.subject = subject;
		this.branch = branch;
		this.price = price;
	}

}
